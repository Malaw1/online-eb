<script>
    function checkRemove() {
        if ($('div.react').length == 1) {
            $('#remove').hide();
        } else {
            $('#remove').show();
        }
    };
    $(document).ready(function() {
        checkRemove()
        $('#add').click(function() {
            $('div.react:last').after($('div.react:first').clone());
            checkRemove();
        });
        $('#remove').click(function() {
            $('div.react:last').remove();
            checkRemove();
        });
    });
</script>
